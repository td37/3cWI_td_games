package at.td.Parcheesi;

public class GamePoint {
    private int x;
    private int y;

    public GamePoint(){

    }


    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
